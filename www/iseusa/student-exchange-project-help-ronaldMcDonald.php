<h2>Brief History</h2>
<div class="two-col">

  <h3>1974</h3>
  <p>The first Ronald  McDonald House Charities opens on October 15, 1974 in Philadelphia thanks to Dr.  Audrey Evans, Philadelphia Eagles&rsquo; player Fred Hill (whose daughter,  Kim, had leukemia), Leonard Tose, owner of the Eagles, Jim Murray, the  Eagles&rsquo; general manager and Ed Rensi, the McDonald&rsquo;s regional manager.</p>

  <h3>1984</h3>
  <p>Ronald  McDonald House Charities is officially established in memory of  McDonald&rsquo;s Corporation founder Ray Kroc, a strong advocate for children</p>

  <h3>1985</h3>
  <p>The first European Ronald McDonald House Charities opens in Amsterdam, Netherlands.</p>
  <p>There are more than 88 Ronald McDonald Houses Charities worldwide.</p>

  <h3>1986</h3>
  <p>The 100th Ronald McDonald House Charities opens in Long Island, NY.</p>

  <h3 style="padding-top: 15px;">1992</h3>
  <p>RMHC  creates a new program for the families of critically ill children with  the first Ronald McDonald Family Room, opening at Children&rsquo;s Mercy  Hospital in Kansas City, KS.</p>

  <h3>2000</h3>
  <p>RMHC  launches the first Ronald McDonald Care Mobile in Worcester, MA,  offering cost-effective medical, dental and health education services to  vulnerable children.</p>

  <h3>2011</h3>
  <p>The first Ronald McDonald House opened in Thailand, making it the 309 Ronald McDonald House in the world.</p>

  <p>The RMHC Donation Box program celebrated its 20th anniversary. As the  largest ongoing fundraiser for the Charity, $200 million has been  collected to date.</p>

  <p>Kim Hill, the girl who inspired the first Ronald McDonald House, passed away at the age of 44. Read her inspirational story.</p></div>
