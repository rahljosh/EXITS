<h1>About Switzerland</h1>
<div class="blueLine"></div>
<div class="ImgR" style="width: 247px;"><img src="images/outbound/switzerland-1.jpg" width="247" height="189" /></div> 
<p>Switzerland is a landlocked European Federal Republic geographically divided the Alps and the Swiss Plateau and the Jura Mountains. The majority of Switzerland’s population of 8 million reside on the plateau, where most of the country’s major cities are found, including Geneva and Zurich. While Switzerland has no true national language of its own, but its official languages include French and German.</p> 
<p>Switzerland’s climate varies by region and altitude, with the Alps forming a barrier which can result in very different weather systems from the north to the south. As a whole, however, Switzerland’s climate is temperate, with cold, snowy and rainy winters and warm, humid summers. The Swiss are an active people. Skiing, snowboarding and other winter sports, including hockey are favorite pastimes among the Swiss. In the warmer months, soccer, tennis and sailing are also very popular.</p> 
<p>Secondary schools in Switzerland are unique in the sense that students are separated according to their learning abilities and capacities. Swiss secondary schools are known as “Kantonsschule” or “Gymnasium.” Featuring schools geared towards, math and science, music and art, languages, and more, Swiss students receive a high school education tailored to their future goals, university studies, and career path.</p> 

