<h1>About Belgium</h1>
<div class="blueLine"></div>
<div class="ImgR" style="width: 250px;"><img src="images/outbound/belgium1.png" width="249" height="290" /></div>
<p>The name 'Belgium' is derived from Gallia Belgica, a Roman province in the northernmost part of Gaul that was inhabited by the Belgae, a mix of Celtic and Germanic peoples.  Historically, Belgium, the Netherlands and Luxembourg were known as the Low Countries, which used to cover a somewhat larger area than the current Benelux group of states. From the end of the Middle Ages until the 17th century, it was a prosperous centre of commerce and culture. From the 16th century until the Belgian revolution in 1830, many battles between European powers were fought in the area of Belgium, causing it to be dubbed the battleground of Europe &#8211; a reputation strengthened by both World Wars. Upon its independence, Belgium eagerly participated in the Industrial Revolution  and, during the course of the twentieth century, possessed several colonies in Africa.  The second half of the 20th century was marked by the rise of communal conflicts between the Flemings and the Francophones fuelled by cultural differences on the one hand and an asymmetrical economic evolution of Flanders and Wallonia on the other hand. </p>

<h3>Education in Belgium</h3>
<p>The Belgian secondary education grants the pupils more choice as they enter a higher cycle. The first cycle provides a broad general basis, with only a few options to choose from (e.g. Latin, additional mathematics, technology). This should enable students to orient themselves in the most suitable way towards the many different directions available in the second and third stages. The second and third cycle are much more specific in each of the possible directions. While the youngest pupils may choose at the most two or four hours per week, the oldest pupils have the opportunity to choose between different "menus", like Math-Science, Sociology-Languages or Latin-Greek. They are then able to shape the largest part of the time they spend at school. However some core lessons are compulsory like e.g. mother-tongue course, sport, etc... This mix between compulsory and optional lessons grouped in menus make it possible to keep class structures even for the oldest students.</p>
