<h1>About Australia</h1>
<div class="blueLine"></div>
<div class="ImgR" style="width: 250px;"><img src="images/outbound/australia1.png" width="253" height="294" /></div>
<p>Australia is a prosperous developed country, with a multicultural society. It ranks highly in many international comparisons of national performance such as human development, quality of life, health care, life expectancy, public education, economic freedom and the protection of civil liberties and political rights. Australian cities rank among the world's highest in terms of cultural offerings and quality of life. It is a member of the United Nations, G20, Commonwealth of Nations, OECD, APEC, Pacific Islands Forum and the World Trade Organization.</p>

<h3>Education in Australia</h3>
<p>Education is compulsory in Australia between the ages of five and fifteen to seventeen, depending on the state or territory, and date of birth.  In recent years, over three quarters of students stay at school until they are seventeen. Government schools educate about two thirds of Australian students, with the other third in Catholic and Independent schools. A small portion of students are legally home-schooled.</p>
<p>Government schools generally charge small fees incurring minor administrative costs, while Catholic and Independent schools charge larger fees. Regardless of whether a school is part of the Government, Catholic or Independent systems, they are required to adhere to the same curriculum frameworks of their state or territory. Most school students wear uniforms,  although there are varying expectations and some Australian schools do not require uniforms.</p>

