<h1>Student Adjustment Cycle</h1>

<div class="dotLine">&nbsp;</div>

  <div class="two-col">
    <h2>1. Arrival and First Days</h2>
   
      <h3 class="h3BlueHeader">The Host Family Feels:</h3>
      <ul> <li> Excited about the new experience.</li>
      <li> Pleased to be showing the student   everything.</li>
      <li> That their privacy may change.</li>
      <li> Anxious about another teenager in   the house and what that means.</li>
    </ul>
    <br><br><br>
      <h3 class="h3BlueHeader">The Host Family Should:</h3>
      <ul><li> Communicate early and often with   the student.</li>
      <li> Try not to overwhelm them with   too much the first few days.</li>
      <li> Encourage the student to ask   questions and use their dictionary.</li>
      <li> SPEAK SLOWLY!!</li>
    </ul>
  </div>
  <br>
  
<div class="blueLine">&nbsp;</div>
 
 
   <div class="two-col">
 	<h2>2. Glory Days</h2>
	<h3 class="h3BlueHeader">The Host Family Feels:</h3>
    <ul><li> That everything is going so well.</li>
    <li> That the student needs to make different/ more friends.</li>
    <li> The student is doing so well and everything is perfect <em>(This is called the halo effect)</em>.</li>
    <li>You don't to put the student on a pedestal.</li>
  </ul> 
  <br><br><br>

    <h3 class="h3BlueHeader">The Host Family Should:</h3>
      <ul><li> Encourage the student to join clubs and activities.</li>
    <li> Arrange for activities to meet the student's friends.</li>
    <li> Talk to the student about his/her friends and cultural experiences and differences.</li>
    <li> Avoid putting the student on a pedestal, it's too easy to fall off.</li>
  </ul></div>
  <br>
  <div class="blueLine">&nbsp;</div>
  
  <div class="two-col">
    <h2>3. Culture Shock</h2>
    
      <h3 class="h3BlueHeader">The Host Family Feels:</h3>
     <ul><li> Hosting as a novelty is wearing off.</li>
      <li> Children may resent the exchange student.</li>
      <li> This may be different from the experience they anticipated.</li>
      <li> That the student is spending too much time by themselves, on the phone, or computer.</li>
    </ul>
      <br><br><p>&nbsp;</p>
      <h3 class="h3BlueHeader">The Host Family Should:</h3>
      <ul><li> Encourage the student to join clubs and activities.</li>
      <li> Talk to the student about their friends and cultural experiences/differences.</li>
      <li> Talk to the student about what the family feels.</li>
      <li> Talk to the Representative - he/she can help.</li>
      <li> Talk to the student about their feelings.</li>
    </ul>
  </div>
  <br>
  
    <div class="blueLine">&nbsp;</div>
  <div class="two-col">
  <h2>4. Feeling Dissatisfaction</h2>

    <h3 class="h3BlueHeader">The Host Family Feels:</h3>
      <ul><li> Halo effect is gone.</li>
    <li> The student and the exchange experience is seen more in the light of day to day behaviors.</li>
    <li> Frustration at the “little things”.</li>
  </ul>
  <br><br><br>
    <h3 class="h3BlueHeader">The Host Family Should:</h3>
    <ul><li> Watch for unresolved conflicts that could lead to more problems.</li>
    <li> Address concerns with each other as soon as possible.</li>
    <li> Contact your Area Representative with any concerns.</li>
  </ul>
  <br></div>
  
  <div class="blueLine">&nbsp;</div>
  
  <div class="two-col">
    <h2>5. Making Adjustments</h2>
    
      <h3 class="h3BlueHeader">The Host Family feels:</h3>
      <ul><li>Less responsible for the student's comings and goings.</li>
      <li> The student is more independent but still part of the family.</li>
    </ul>
    <br><br><br>
      <h3 class="h3BlueHeader">The Host Family Should:</h3>
      <ul><li> Encourage the student to meet new people and try new activities.</li>
    </ul>
    <p>&nbsp;</p>
    <br>
  </div>
  <br>
  
    <div class="blueLine">&nbsp;</div>
  
  <div class="two-col">  
  <h2>6. Feeling Accepted</h2>
    <h3 class="h3BlueHeader">The Host Family Feels:</h3>
    <ul><li> That the student is relaxed with the family and is able to tease and joke with them.</li>
  </ul>
  <br><br> <p>&nbsp;</p><p>&nbsp;</p><p>&nbsp;</p>
    <h3 class="h3BlueHeader">The Host Family Should:</h3>
    <ul><li> Help the student find ways to remember the exchange experience (scrapbooks, photo albums, etc.)</li>
    <li> Encourage their student to write letters, if appropriate, to the newspapers to thank the community and school for their experience.</li>
    <li> Look for the student to become more social during this time.</li>
  </ul>
  </div>
  
    <br>
   <div class="blueLine">&nbsp;</div>
  
  <div class="two-col">
    <h2>7. Return Anxiety</h2>
    
      <h3 class="h3BlueHeader">The Host Family Feels:</h3>
      <ul><li> Anxious about sending the student back home to his/her home country.</li>
      <li> Uncertain whether the student will remember the family upon his/her return home.</li>
      <li> An urge to "capture" every moment left with the student before they leave.</li>
      <li> Jealous of the time others demand of the student away from the family.</li>
      <li> Sad because they will miss the student when the student leaves.</li>
    </ul>
    <br><br><p>&nbsp;</p>
      <h3 class="h3BlueHeader">The Host Family Should:</h3>
      <ul><li> Set aside time to talk about all of the student's feelings.</li>
      <li> Assure the student that all of the feelings are normal in this situation.</li>
      <li> Reassure the student that leaving does not mean "goodbye."</li>
      <li> Plan out your future communications with your student.</li>
      <li> Call your Area Representative and talk about it.</li>
    </ul>
     <p>&nbsp;</p>
     <p>&nbsp;</p>
  </div>
