<h1>Facilitator Square</h1>

<p>With  the move to the on-line application and the creation of the compliance  department, the role of facilitator has transitioned to become a field  support role and the primary point of contact for the field to the New  York office. If you have a question about an application, payment,  student support issue, or any other question, call or email your  facilitator and they will get you an answer.</p>
<p>I's  also very important to make sure that your region is prepared for the  year. Below is a checklist of items that your reps, host families and  students need to double check.</p>

<p><strong>Registering for high school: </strong>Make sure your student takes care of all your school's requirements for registration. They will likely require proof of vaccination/immunization documentation and other documentation.</p>

<p><strong>ISE Insurance Info:</strong> Please  go over all insurance information with your student when they arrive so  that you are prepared in case of emergency. Visit the insurance website  to find a list of local doctors that they can use, perhaps the same one  as the host family.</p>

<p><strong>Important Documents:</strong> Keeping important documents (visa, passport, etc) in a safe place that the host family is aware of in case of an emergency. The insurance card should stay with the student at all times and a copy needs to be with the host family and maybe even the area rep.</p>

<p><strong>Communication:</strong> Encourage the host family to contact the area rep, no matter how big or small the issue. The area rep should be aware of the little things because sometimes they turn into big things.</p>

<p><strong>First Day of School:</strong> Ask a guidance counselor to assign your student a buddy for the day to show them around school or to sit with him/her at lunch. Encourage your student(s) to get involved in activities early on in order to make friends and adjust more quickly. When choosing classes make sure students are taking an English and American History course as well as two other academic classes. It is good to balance out the heavily academic courses with some lighter elective courses as well. Be sure to find out if there is a class that your student needs to take in order to receive credit back home.</p>

<p><strong>Establish House Rules:</strong> Make sure your families establish rules and expectations with their exchange student early on. Review ISE rules and program policies in the ISE student handbook. Focus on key issues of concern, curfews, chores, internet and phone usage, getting around, etc.</p>

<p><strong>Adjustment:</strong> Many students go through feelings of homesickness when they arrive and experience highs and lows in their first few weeks or months. Your host families will also notice adjustment issues as they take on a new family member.</p>
