<h1>Compliance Corner</h1>
<p>The online application has been going very well. We want to thank you for all your support and patience in making this a successful transition. The new application has significantly sped up the approval process and most applications are approved the first time through the compliance process. This has allowed the Compliance team to maintain a consistent 24-hour turnaround, with many applications being approved the same day they are received.</p>
<p>What slows down the process is when the application has missing or inconsistent information and then needs to go back to the Area Rep to clarify an issue. While it may seem that these issues are small, we are subject to review and audit by the US Department of State and they pay attention to every detail in the application, <strong><u class="red">no matter how small</u></strong>. That means that even if the placement is with a wonderful family and a great school, we can be cited because we were missing a photograph or because of an incomplete answer to a question.<br /></p>
  <h3>The most common issues we find are:</strong></h3>
<ul>
  <li>Inconsistent information in the application &#8211; especially around children and pets.
<p style="padding-top: 10px;"><strong>Examples include:</strong></p>
<ul>
    <li>The application lists no pets, but the family letter says &quot;we love playing with our three dogs&quot; or the family album has a family photo where they are holding a cat or dog.</li>
    <li>The host family application lists the pet information; however the Confidential Host Family Visit Form either lists no pets or incorrect pet information.</li>
    <li>The application will say that the exchange student will not be sharing a room, but the family has a three bedroom home and five children.</li>
</ul></li></ul>
<ul>
  <li><strong>References &#8211; improper or incomplete references</strong>
  <ul>
    <li>References must be from people that live in the community or general vicinity of the host family</li>
    <li>Missing answers &#8211; especially question 3 &#8211; &quot;Characteristics of the Home&quot;. Consider answers like, &quot;the home is neat and clean&quot; or &quot;inviting home&quot; or &quot;kids love visiting their home&quot; or &quot;happy home environment&quot;, etc.</li>
  </ul></li></ul>
  
  <ul>
  <li><strong>Double placement &#8211; we need either:</strong>
  <ul>
    <li>Photos for the two bedrooms for the exchange students</li>
    <li>Photo of two beds in the bedroom</li>
  </ul></li></ul>
  
  <ul>
  <li><strong>Photos</strong>
  <ul>
    <li>Photos must be clear and the rooms should be tidy </li>
    <li>Host family photos should only contain the family members. Photos showing extra people will be questioned to ensure they were not left off the application.</li>
    <li>Photos should not be dated and must be current.</li>
  </ul></li></ul>
  
  <ul>
  <li><strong>Incomplete or inaccurate </strong><strong>school acceptance forms</strong><strong> </strong>
  <ul>
    <li>Missing school contact title and email address, website and school dates.</li>
    <li>Errors in the student's DOB, incorrect spelling of host family names, addresses and the student's name.</li>
  </ul></li>
</ul>
<p>As we all know, we are headed for crunch time and hope these tips help you to submit complete applications so that you can meet all placement and bonus deadlines.</p>
<p>Please also note that per Department of State regulations, the host family orientation must occur <strong><u class="red">after</u></strong> the completion of the application by the host family&nbsp;and <strong><u class="red">prior</u></strong> to the student's arrival.</p>
<p>Just a reminder, the Compliance Department email address is <a href="mailto:compliance@iseusa.com">compliance@iseusa.com</a>.</p>
