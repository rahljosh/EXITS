<h1>Compliance Corner</h1>
<p>We are getting ready for the new placing season and looking forward to it.  Our goal is to be more efficient and streamline some of our procedures to process so that we can approve your applications as quickly as possible.  The changes to the online application should help in this area, so please familiarize yourself with them.</p>
<p><strong>January Placements and Orientations</strong></p>
<p>If you&rsquo;ve made January placement, please get your host family and student orientations to us as soon as possible and within the window for the orientation dates.  Please also note that we won&rsquo;t process any supervision payments until we&rsquo;ve received your required orientations.</p>
<p><strong>Relocations</strong></p>
<p>The host family application must be completed in its entirety before the student is relocated.  It is a regulatory requirement that we have a completed, approved application in EXITs before a move takes place.  Failure to do so is a regulatory violation and can result in Department of State sanctions.  Compliance prioritizes relocations and complete applications are processed on the same day so the approvals do not delay the move process.</p>
<p>Also, when placing a student in a family who is already hosting a student, please remember we need a second bedroom photo.  Too often, this photo is missing, thereby delaying the application approval.</p>
