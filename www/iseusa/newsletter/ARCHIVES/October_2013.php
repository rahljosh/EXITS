<h1>Facilitator Square</h1>
<h2>Communication is the Key!</h2>
<h3 style="color: #000;">Notify your Regional Manager of <span class="red"><u>All</u></span> student issues, no matter how small</h3>
<p>With the year well underway, most of our students are adapting well to their homes, communities, and schools and new language. For those that are still having challenges we want to remind everyone that is <u>very important</u> that you <u>notify your Regional Manager (RM)</u> of any problems your student(s) may be experiencing. Your RM will then help determine the best course of action and will also report these incidents back to the home office.</p>
<p>Knowing what is happening early on is critically important. In the home office, we often see situations where the first thing we hear is a host family saying that they want a student "sent home" or a student is being dismissed from school and we've never heard of any incident in the home. We contact the RM who's also heard nothing. They then contact the Area Rep who says that the student has been misbehaving for weeks or months. In those cases, it limits our options since we have not gone through the proper warnings to the student or notifications to the agent and natural family It's only fair to give the student a chance in these cases and when we haven't lived up to our responsibilities.</p>
<p><strong>Please do not contact or communicate with natural family members or relatives</strong></p>
<p>Keep in mind that while you are working directly with a student in your area, that student has natural parents and a foreign agent that need to be made aware of the progress or their child. That is the responsibility of the ISE home office and our international agents. <strong>Please do not communicate with the natural parents</strong>. While you think you may just be trying to help out the situation, our home office and foreign partners are in the best position to manage communication with parents from different cultures. If a natural parent contacts you, please tell them to contact their international agent. This is something they agree to this in their contract and it assures that the situation is being dealt with appropriately.</p>
<p><strong>Do not move students until the new family is approved by the home office</strong></p>
<p>There nothing more embarrassing and unprofessional than telling an agent or natural family that ISE does not know where there child is staying. It undercuts our credibility with our natural families and our international partners. Additionally, United States Department of State regulations only allow placement after the host family has been fully vetted and their application has been approved by the home office. Students can be moved only after the new family application has been approved by the home office.</p>
<p>In emergencies, students should be moved into your home, or into the home of an existing <u>vetted</u> host family. Even in these emergency situations you must <strong>immediately notify</strong> your RM. Call them on your way over to pick up the student so that your RM can then inform the home office of the move. The home office will then notify our international agent and the natural family.</p>
<p><strong>Monthly Progress Reports</strong></p>
<p>Monthly progress reports are a key tool in keeping the natural family aware of how well their child is progressing on the program. They should be done each month and don't just wait until the "payment" month. Keep in mind that we do share these reports with the family so please provide detailed responses to each question. Responses should be <u>paragraph length</u> and provide detail and examples. Use professional language and proper grammar and spelling.</p>
<p>Please also make sure the report reflects what is really happening in the home. If there are issues, share them in the report. It often happens that we get an email from an Area Rep about a student failing school or having significant host family issues. When we look at the progress report, it says "student is doing well in school"; and "getting along well with host family."</p>
<p><strong>Change in payment process</strong></p>
<p>Starting this week, we'll implement a new payment process for progress reports and second visit payments. Payments will be made on a weekly basis for all supervision reports that are completed in the previous seven days. Checks will be cut every Wednesday and you'll receive a report of all progress report and second visit payments each Thursday. Payments will be made only when the home office has received ALL required documentation, including orientation paperwork. If there are payments that you feel are missing from that report, please review EXITS to ensure that all paperwork has been received. If it has, and you still haven't been paid for an item, contact your RM who will then follow-up with the home office.</p>
