<h1>Message from the President</h1>
<div style="width: 225px; float: right; margin: 10px;"><img src="../images/wayne.jpg" width="224" height="161" alt="Comliance Photo" /></div>
<p>On Monday, October 7th, I had the pleasure of honoring the founders of International Student Exchange, Barbara and Bob Fink.</p>
<p>Accepting the award was Julie Maes, Bob's sister and current ISE Manager. Bob and Barbara founded the company in 1982 and Julie started her managerial career as well in that year. Bob was unable to accept the award personally due to Barbara's recent passing.</p>
<p>Bob and Barbara were host parents on several occasions prior to starting ISE in 1982. They were not happy with the exchange company from which their students came and decided that they could do better. They felt that if the exchange program was designed to bring people of the world closer together, than it had to be operated in a qualitative manner.</p>
<p>Thirty years later, ISE is proud to state that it still maintains the same mission that Bob and Barbara put forth and its J-1 Secondary School Program still maintains the high level and spirit that its founders demanded in 1982.</p>
<p>ISE is very proud and grateful for the vision and devotion of its two founders.</p>
