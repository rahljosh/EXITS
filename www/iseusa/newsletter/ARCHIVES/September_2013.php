<h1>Facilitator Square</h1>

<p>With the start of a new program, we want to remind everyone of a few key items.</p>
<p><strong>Host Family Rules</strong><br />
  Make sure your host families establish clear Host Family rules. These should be written down and discussed with the student. Key issues to address include:</p>
<ul type="disc">
  <li>Curfew and contact</li>
  <li>Inviting friends over</li>
  <li>Food and special food issues/costs</li>
  <li>Internet usage</li>
  <li>Phone use and charges &#8211; We recommend that families not include exchange students on their family cell phone plan. This has resulted      in significant charges and problems between families and students in the past when texting, long distance and data usage bills come in.</li>
</ul>
<p><strong>Communication</strong></p>
<ul><li>Make sure your host families know who you are and when to contact you. Many host families, especially those who have not hosted before, don't clearly understand the role of the local area representative. Frequent reminders that you are their main point of contact and ensuring the family has all your contact details can help prevent small issues from growing.</li><br />
<li>Strongly encourage the host family to keep you in the loop on every issue, no matter how small. More often than not, big issues relate to a series of small issues and if they are addressed early on, the larger issues can be avoided.</li><br />
<li>Keep in touch with your students via in-person visits, email, phone, text or other means. Be careful to listen to them and allow them to fully express their feelings and avoid jumping to conclusions. Ask open ended questions. For example:<br />
  <br />
<ul type="disc">
  <li>If a student is      having issues with a host sibling ask questions such as:</li>
  <ul type="circle">
    <li>How's your relationship with your family? </li>
    <li>How are you getting  along with (ask about each member of the family)?</li>
  </ul>
  <li>If they mention an      issue, ask questions such as</li>
  <ul type="circle">
    <li>Why do you think that happened?</li>
    <li>What do you think you can do to make the       situation better?</li>
  </ul>
  <li>Avoid "leading"questions like:</li>
  <ul type="circle">
    <li>I hear you are having problems with . . . ?</li>
    <li>Do you want to be       here &#8211; with this family &#8211; in the US &#8211; On the program?</li>
  </ul>
  <li>These questions can come off as threats, will make students nervous and will prevent you from      understanding the true issues.</li>
</ul></li></ul>
<p>Keep up on your progress reports. We&rsquo;ll be tracking the completion of progress reports and automating the payment process. <br />
  </p>
<p>Please follow the chain of communication:</p>
<ul type="disc">
  <li>Host Family &#187; Area Representative &#187; Regional Advisor/Regional Manger &#187; Home Office &#187; Agent -- We understand that this chain is somewhat long and you may not get      always get answers as quickly as you would like, but skipping steps in      this chain causes more issues than it solves.</li>
  <li>Make sure HF know who      to contact with questions/issues</li>
  <li>Make sure HF and      student have AR contact info</li>
</ul>
<p><strong>Insurance</strong><br />
  Students on the ISE high school program are covered under an insurance plan issued by Global Secutive and claims are managed by United Healthcare. Please take a moment to visit the Global Secutive website (<a href="http://www.esecutive.com">www.esecutive.com</a>) and review options for doctors in your area. Please also identify a local urgent care clinic. While all communities do not have urgent care clinics, it is usually a more preferable option than an emergency room as the service usually happens faster and students aren&rsquo;t subject to special emergency room visit charges. Urgent care clinics can usually treat sprains, broken bones, minor cuts and burns and other non-life threatening situations and emergency rooms are better suited for significant injury, breathing difficulties and other significant emergency health concerns. Please contact the ISE office if you have any insurance questions at <a href="tel:800.766.4656" target="_blank">800.766.4656</a>. Please have your student make a copy of their insurance card and keep the copy in a safe place and make sure the student keeps the card with them at all times.</p>
  <P style="padding: 10px; background-color: #EFEFEF; text-align: center;"><strong>Global Secutive:</strong> <a href="http://www.esecutive.com/MyInsurance/">www.esecutive.com/MyInsurance/</a><br />
  <strong>ISE Contact:</strong> 800.766.4656</P>
<p><strong>Language Skill</strong><br />
  If you have a student who is facing challenges with their English ability contact your RM immediately. In many cases if we address this situation early on, we can involve tutors and other support to help resolve this issue.<br />
  </p>
<p><strong>Keep Active</strong><br />
  Get students involved in activities at the school and in the community immediately. Don't let them get settled. Get them out of the home and joining clubs, local youth groups, sports teams and other organizations.</p>
<p><u>Issue Documentation and Resolution</u><br />
  If student issues do arise, please make sure you document them via the Problem ID Form (PID), the Problem Resolution form (PRF), and the Notice of Probation (NOP). <br />
</p>
<p><strong>PID </strong>&#8211; the PID is used to document any student issue. It's completed by the Area Rep and sent to your RM and the facilitator. It does not require any signature or necessarily any follow-up.<br />
  </p>
<p><strong>PRF </strong>&#8211; the PRF is used to document a meeting that you have with the student and host family and get honest feedback from the student. It is of the utmost importance that the student provides some feedback, regardless of whether they agree or disagree with the proposed solution. If the student is not comfortable giving feedback to you or the host family, have them send a confidential email to their facilitator. </p>
<p><strong>NOP </strong>&#8211; Notices of probation are used for very serious situations where the student is in jeopardy of being removed from the program. They should be issued only after consultation with the your Regional Manager and the facilitator.</p>
