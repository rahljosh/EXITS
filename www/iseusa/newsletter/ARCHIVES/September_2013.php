<h1>Message from the President</h1>
<div style="width: 225px; float: right; margin: 10px;"><img src="../images/wayne.jpg" width="224" height="161" alt="Comliance Photo" /></div>
<h3>A New Beginning</h3>
<p>One of the nicest things about being involved in student exchange is that each and every September brings forth a new season.  Of course, it is wonderful to greet the students, watch the relationships grow, and to see all of the work you put forth start to blossom at school and with the host families.  But to be able to start the process all over is something most work and projects don't have the capability of doing.</p>

<p>I have always encouraged early September to be the best time to make those lists of things to do such as reports, supervision, activities, and of course preparing for the new season ahead.  It is always a great time to look back and see what can be improved upon and make those changes.  September is the perfect time to enjoy the students and their families and to help out those who are in need of extra time and counseling to make those necessary adjustments.  I have always told myself that I need September not only to get myself organized and prepared but it has always given me the positive impetus to go forward and try and find those families for the coming January and August seasons.  When we see all of the wonderful and positive results of our work, it should motivate us to even want to do better.</p>
<p>To find an experience where you can start all over each year is rather unique.  Take advantage of this and commit yourself to a wonderful new season.  To be able to bring people of the world together and help the leaders of tomorrow grow, just adds to the fresh start that September brings us.</p>
<p>Sincerely,</p>
<h2 style="font-family: 'Yesteryear', cursive;">Wayne Brewer</h2>
