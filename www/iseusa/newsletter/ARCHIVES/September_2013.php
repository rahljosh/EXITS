<h1>Compliance Corner</h1>
<p>The Compliance Department put in many long hours throughout the month of August and the beginning of September to insure all online applications and school acceptances were reviewed as quickly as possible. ISE has done an <strong>excellent job</strong> with placement paperwork this year. All applications are in and we're just waiting for a handful of missing items.</p>

<p>
Thank you to all the field staff for your hard work in getting the host families to complete their online applications. We especially appreciated how responsive everyone was when we had a quick question or needed additional detail to finalize approval of the application.</p>
<p>We have been receiving many host family orientations and student orientations. It is important to remember the host family orientation must be done before the student arrives and dated as such and the student orientation is of course done after the student arrives and dated after arrival. This may seem like something that is too obvious to point out, but we often see incorrect dates on the orientations.</p>
<p>As we mentioned earlier this year, the second visit report processing and payments will be automated. Compliance will be reviewing these reports and once they approve them, payments will automatically be generated. The detail in the second visit report will be compared to the Confidential Visit Report (aka the host family visit report) and if there are discrepancies, compliance will be contacting you for clarification.</p>
<p>With all the students arriving and adjusting to their new surroundings, there will of course be some situations where the placement does not work out and we need to relocate a student. If this happens, please make sure that he facilitator is notified prior to any move decision being made. Please also keep in mind that regulations require us to fully vet the new host family prior to the student being moved so please get any new relocation family to complete a new online application. However, if a student is being moved to a host family that has already been approved for the 13/14 season, no new application is required.</p>
<p>As always, if there are urgent situations where an application needs to be reviewed immediately, please let the Compliance Department know. We can usually turn these applications around within an hour, assuming all the documentation is completed.</p>

