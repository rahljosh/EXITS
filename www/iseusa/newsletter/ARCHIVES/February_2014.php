<<h1>Employee Spotlight</h1>

<h3><strong>Tom Policastro</strong>&#8211; <strong>Project Manager</strong></h3>
<div style="margin: 10px; width: 235px; float: right;"><img src="../images/tomEmployee.png" width="235" alt=""/></div>
<p><strong>Q: Tell everyone about yourself. What is your background, interests and hobbies.</strong></p>
<p class="grey"><em> <strong>A:</strong> I grew up on Long Island, with all of my formative years worth writing about spent in sometimes painfully uninteresting suburb of Miller Place. I kept busy honing a jump shot, running the school’s drama program, and playing music with good friends. I studied professional writing at York College of Pennsylvania, with most of my extra circulars being music based. After a brief respite in Lancaster, PA post grad, found my way back to Long Island’s sandy shores.  I spent just about a year to the day working in customer service for a local grocery store and playing gigs by night when time allowed. I then came across an ad in the paper for a position with ISE and, as the saying goes, the rest is history.  In my spare time I still make music when I can, but a lot more casually than I used to. I sing vocals primarily, though, I do dabble with the ukulele, but "good look" getting me to strum that in public. I play basketball, read, and write quite a bit, (plays, short fiction, album reviews, etc.). I keep pretty busy and my needs are few. As long as there’s good people and laughter around, I’m pretty easily contented. </em></p>
<div class="hrLine"></div>
<p><strong>Q: What is your job function in ISE?</strong></p>
<p class="grey"><em><strong>A: </strong>I’m a Project Manager for ISE, which having not yet been through I full cycle in student exchange, I still find a bit difficult to properly, or at the very least, concisely define. I will say that a lot of my day to day obligations entail collaborating with our International Agents and making sure their questions are answered and needs are met. I wind up planning a lot of events and trips, not the least of which being our “Celebration of Success,” which is one of my few opportunities to work with and get to know the field. The program that I manage and have worked hard to revamp a bit is our Outbound Program, which I look forward to continuing to help grow. My job description kind of changes with the seasons and renders me a Jack of a great many things and a master of very few. With that said, so far, I enjoy the unique diversity this job offers and I’m learning a ton about this ever-evolving industry by the day. I’m never bored, always on my toes, and I appreciate the variety of skills sets that this job allows you to cultivate. </em></p>
<div class="hrLine"></div>
<p><strong>Q: What is it about your work that is the most challenging/satisfying? </strong></p>
<p class="grey"><em><strong>A: </strong>Sometimes the ever-changing landscape of my job description feels a bit more like a curse than a blessing, but as I said, I’d much prefer the excitement to monotony. I would say the biggest challenge of my job, or adjustment I’ve had to make as a professional, is something I think we can all relate to in this business. My biggest adjustment by far has been adapting to this 24/7 kind of industry. I’m no stranger to hard work and in my heart I’d never be fulfilled at a job that didn’t require my full attention and effort, but it was pretty jarring at first to realize just how much this industry’s expectations clashed with the traditional 9 to 5. I wouldn’t go so far as to say it’s something I’m “used to” by now, but it’s certainly a facet of the job I’ve come to expect and I take great pride in rising to that challenge. What that lone aspect of student exchange has taught me about myself has been invaluable. What I adore about this job is how tangible the results are. There are so many occupations out there where people bust their hump and might not get the chance to see the fruits of their labor. ISE appears to function in exactly the opposite way. When you take on a project, you get the chance to see it through to the end and the results are empirical and intensely gratifying. As cliché as it sounds we help young people see the world and better their lives beyond their time abroad and often we have the chance to get to know these kids along the way.  A mission statement like that makes the tough days worth it, I find.</em></p>
<div class="hrLine"></div>
<p><strong>Q: Tell us something we don't know about you. </strong></p>
<p class="grey"><em><strong>A: </strong>I’d like to believe we’re all a little deeper than we seem.  I suppose in my case the music and writing ventures take people by surprise.  I used to gig a bit more and my creative writing has been published a few times, just not enough to fill a bank account just yet.  I used to freelance for a few PA based websites about NBA basketball, but I no longer really have the time to invest in breaking all that stuff down like a used to.  I will say that my dream job would be to become a writer for Sesame Street.  I reckon that would fall under the “fun fact” category.</em></p>
<div class="hrLine"></div>
<p><strong>Q: What new projects are you going to be working on this year?</strong></p>
<p class="grey"><em><strong>A: </strong>The Outbound Program is one of the few year-round constants for which I’m responsible.  I look forward to building that facet of ISE up again.  I have made great strides in cleaning up and organizing the program. The re-designed website has also helped usher in this new era for Outbound. The bottom line is I still have so much to learn about Outbound and student exchange as a whole, but if there’s one thing I’ve proven in my brief time here it’s that I care about ISE and a bit more selfishly, what the quality of my work says about my character. I look forward to growing with ISE and returning the favor by helping ISE grow and excel wherever and whenever I can.</em></p>
