<h3>Payment questions</h3>
<p>We often get question from Area Reps about why they didn’t receive a payment.  Nine times out of ten, the answer is that some required document is missing.  If you have completed a progress report or submitted a placement and haven’t been paid yet, please check the following items before contacting your Regional Manager or the ISE office.</p>
<p><strong>Progress Reports:</strong> The progress report needs to be approved by your regional manager and facilitator before it will be paid.  Additionally, all documentation must be received and approved by the ISE office before a payment is made.  Make sure that the student and host family orientations are complete, especially when a student moves to a new host family.</p>
<p><strong>Placement Payments:</strong> Placements are paid when the placement has been approved by the ISE home office and all documentation has been submitted and approved.  The most frequent cause of payment delay is a missing school acceptance.  Please make sure that these are submitted so you can ensure the fastest payment processing.</p>
<p><strong>Timing: </strong> Checks are cut each Monday and Thursday and are typically sent out the following day.  If you complete a progress report or submit an application, please keep in mind the approval time as well as the mailing time for the check.  So if you submit a missing school acceptance on Monday, it will probably take a week or more before you receive a check.</p> 
<p><strong>Status:</strong> If you ever want to check the status of a payment, you can look up your payments in EXITS.  Select “Users → My Information” from the menu bar and scroll down to “Payment Activity”.  If you click on the “view details” link, it will give you access to your payment history.  Note that in some cases you may see a payment date in the future.  That means that a check is forthcoming on that date.</p>
