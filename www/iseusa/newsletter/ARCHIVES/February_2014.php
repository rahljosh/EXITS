<h1>Compliance Corner</h1>
<p>The quality of the content in the host family application and supporting documents has improved tremendously and there are just a couple areas we can still use a little more attention.  Getting these items right will ensure the fastest possible processing of your host family applications.</p> 

<h3>HOST FAMILY APPLICATION:</h3>

<H4>Personal Description</H4> 

<p>As the application states, the personal description is one of the most important parts of the application.  It introduces the family to the student and establishes the initial bond between them.  The description should include the following:</p>
<ul>
<li>Intro of why the family wants to host.</li>
<li>Intro of each family member with a couple of sentences Other details or interesting facts about the family.</li>
</ul>

<p><strong>Here is an example of a good letter:</strong></p>
<p style="margin-left: 25px"><i>Carl and I are faith and family focused and are blessed to have healthy, well-rounded children who understand the importance of service in our community. Our family loves to spend time together supporting each other’s interests such as acting, drawing, soccer, basketball, and any other inspiring projects.</i></p>  

<p style="margin-left: 25px"><i>Carl works at a local steel mill providing for his family.  He is also an assistant coach for the high school girl’s soccer team.  He volunteers a lot at the local homeless shelter (where I work) and is a youth group leader at our church.  His passion is cooking.  He loves to cook, especially old family recipes. </i></p> 

<p style="margin-left: 25px"><i>I (Lily) was lucky to follow my dream of making a living by serving others.  I currently work at a local non-profit whose focus is to provide safe housing for homeless and food through a food pantry.  I am also a youth leader at our church as well as the committee chair for the Peace and Social Justice group.  I also sit on a county wide committee to end homelessness in our county.</i></p>  

<p style="margin-left: 25px"><i>Our oldest son, Kyle, is 23 and lives independently in Burlington, which is close to our town of Essex.  He is enrolled in culinary school and works at a local restaurant as a cook.  Our son, Jacob, is 18 and a senior at Essex High School.  He dreams of attending an art school after he graduates.  He enjoys playing soccer and currently works part-time at a local fast food restaurant.  Our 3rd son Brad, is 16 and is very active in our church youth group.  He plays basketball and soccer through the school.  Our youngest son Peter, is 14 and lives for soccer!  He plays year-round for a premier league and also very active in the church youth group.</i></p>  

<p style="margin-left: 25px"><i>We are a smoke-free home and encourage our children to be leaders in our community by living a positive lifestyle and being an example for others.</i></p>  

<p style="margin-left: 25px"><i>We love to learn about other cultures, religions and geographic areas.  We are interested in hosting an exchange student to share our culture and learn about theirs.  We hope to be a welcoming home and a safe place to give a visitor to the U.S. the best experience while they are here.</i></p>

<h4>Family Album</h4>

<p>All photos must be original photographs.  Since you have to visit the home anyway, please bring a camera with you and take a photo of the host family members and home during your visit.  This will ensure that you have current photos.  Photos that are older than one year will be rejected.</p>

<p>The home photos must show tidy rooms and make sure they include the entire detail of room.  For example, a bathroom photo should not just show a toilet.  Sometimes this is hard to do with one photo so please take multiple photos if necessary during your visit to make sure you capture all aspects of each room.</p>

<h4>References</h4>

<p>References cannot be relatives.  They must have visited the family inside of their home and they must live in the same community or general vicinity of the host family.</p>

<p>If a reference listed is not in the same town as the host family, the Compliance team will calculate the distance from the town host family resides to the town of the reference.  If the reference is more than an hour away from the host family, we will have to request another reference.</p>  

<h3>HOST FAMILY ORIENTATIONS:</h3>

<p>The DOS rules state "sponsors must conduct the host family orientation at the end of the family application process, i.e., after the host family has been fully vetted and accepted into the program."</p>  

<p>This means the orientation must take place after the Compliance team has approved the host family application.  Therefore, the date of the orientation must be after the application HQ approval date to be compliant.  Orientations with non-compliant dates will not be accepted and will hold up supervision payments.  Due to the volume of orientations, we cannot notify each rep of a non-compliant orientation.</p>  
<h3>SCHOOL ACCEPTANCE:</h3>

<p>We are still receiving school acceptances missing information.  If there is a question on the form, there must be a answer (this is what the DOS has told us).  The most common issues are:</p>
<ul><li>host family and school complete address  (street,  town, state, zip code)</li>
<li>host family email address missing</li>
<li>school website missing</li>
<li>school contact title missing</li>
<li>school beginning and end date</li></ul>
<p>Submitting an incomplete or inaccurate school acceptance will hold up a student's placement and the placement payment to the area rep.</p>
<h3>DOUBLE PLACEMENTS</h3>
<p>Due to the question in the 14/15 host family application on the Hosting Environment page regarding the host family's option to host two students, we no longer require a host family double placement document.  By selecting "Yes, I would like to host a second student", that is the approval.</p>

