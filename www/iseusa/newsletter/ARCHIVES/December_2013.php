<h1>Message from the President</h1>
<div style="width: 225px; float: right; margin: 10px;"><img src="images/wayne.jpg" width="224" height="161" alt="Happy Holidays from ISE" /></div>

<p>As the holidays approach, everyone involved in student exchange enjoys the greatest of all seasons. Our mission statement of &quot;bringing people of the world together&quot; is never more obvious than this time of year. The different customs, cultures, traditions and activities present a great example of what we can all learn from each other. Most Americans have never even heard of a German Christmas Market while most Europeans have never seen a typical decorated Christmas tree packed into a home as many people do in the United States.</p>
<p>The exchange experience gives us all the opportunity to gain insights into other people and nationalities. One of the reasons why international diplomacy often does not work is that there is so much basic misunderstanding among different cultures of the world. As we know, our American families often gain as much from the hosting experience as our foreign students. We must learn how other people live and think. It is nice to see how our host families and exchange students have learned to overcome worldwide problems simply through the exchange experience.</p>
<p>So as the holiday season comes upon us, let us all be thankful for this wonderful experience of hosting. Our host families and our foreign students can all enjoy that incredible feeling of being together. They grow to understand, relate and love each other. This is a great time of the year to thank our representatives, our families, schools and our foreign friends for joining together in this most wonderful exchange program.</p>
<p>Sincerely,</p>
<h2 style="font-family: 'Yesteryear', cursive;">Wayne Brewer</h2>
