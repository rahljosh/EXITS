<p><strong>Exchange Holiday Traditions!</strong>&nbsp;</p>
<p>The holiday season is one of the most exciting times of the year for host families and their exchange students. Students are off from school and get to spend more time with their host families. They also often get the opportunity to meet many of your relatives and other out-of town guests. You'll of course want to share your holiday traditions with your student and they also may have traditions from their home countries they want to share with you.</p>
<p>You should also keep in mind that since the holidays are a family time, students may have a bout of "holiday homesickness" since they are far from their natural family. This is not uncommon and the best way to help your student is to keep them involved with your family's activities and make sure your student is not spending excessive amounts of time contacting family and friends at home.</p>
<p><strong>Five month students returning home and Convalidations</strong></p>
<p>It may seem like they just arrived, but students on the fall semester program may soon be leaving. While these departures can be emotionally trying, the bonds you've built over your time together will be long-lasting.</p>
<p>If you've hosted a semester student from Brazil, Ecuador, Italy, Spain or Mexico, you may have already heard them mention something about a "convalidation". A convalidation is an official certification of a student's transcript that they will need to present to their home school in order to get credit for the classes they took in the US.</p>
<p>It is the students' responsibility to take care of their own convalidation process, but they may need your help in securing some of the documents since they will often have to return home before their transcript is available. Usually, the host family just needs to forward the notarized transcript to the exchange student. </p>
<p>The convalidation process varies from country to country and links to the process are here:</p>
