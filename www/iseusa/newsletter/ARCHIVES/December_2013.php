<h1>Compliance Corner</h1>
<h3>Happy Holidays from the Compliance Team!</h3>
<p>We appreciate all the effort you have made to improve the quality of the information in the online applications. We'll  be sending out some updates to the host family application and process in a couple weeks. In the meantime, we thought we'd share with you some  key items to consider as you start recruiting and placing AYP 14/15 and Fall 2014 semester students.</p>
<p><strong>Existing host families </strong>&#8211; If a host family has already completed an eHost application, they just need to refresh their application. This includes:</p>
<ul type="disc">
  <li>Updating all photos</li>
  <li>Updating personal description</li>
  <li>Noting any changes to family composition (family members moving out, new additions, moves, etc.)</li>
  <li> Updating any references</li>
  <li>You will still need to do a home visit and host family orientation</li>
</ul>
<p><strong>Both new and existing host families </strong>&#8211; The following items apply to both new and existing host families: </p>
<ul type="disc">
  <li>The  entire host family must be approved before a student can be placed with  the family. However, you can place a student prior to receiving the  school acceptance form.</li>
  <li>Make  sure that all information in the school acceptance is completed. Missing information delays approval of the placement and requires more  work for everyone. Common issues include:</li>
  <ul type="circle">
    <li>Missing  or incomplete addresses for the host family and school. Make sure you  have the street address (not a PO Box) for both the family and school.</li>
    <li>Missing school information:</li>
    <ul type="square">
      <li>Make sure you get the school contact&rsquo;s title (Principal, Counselor, etc.)</li>
      <li>School website address (e.g. <a href="http://www.yourschool.edu" target="_blank">www.yourschool.edu</a>)</li>
      <li> Include start/end dates for both semesters</li>
    </ul>
  </ul>
  <li>First  impressions count. The host family photos are included in the host family application that is shared with our agent, exchange student and  his/her natural family.</li>
  <ul type="circle">
    <li>Photos must be original photos and not scans of old application photos or pictures of framed photos in the family home.</li>
    <li>Take nice bright photos of the home</li>
    <li>Make sure the rooms in the home are tidy and no people are present</li>
    <li>Get pictures of the family in action (travel, hobbies, events)</li>
  </ul>
  <li>Common issues with references:</li>
  <ul type="circle">
    <li>References cannot be relatives</li>
    <li>References must have visited the inside of the home</li>
    <li>References must live in the same community or general vicinity of the host family.</li>
  </ul>
  <li> Relocations to a double placement &#8211; please remember to:</li>
  <ul type="circle">
    <li>Link the students as a double placement</li>
    <li>Get an additional bedroom/bed photo</li>
    <li>Make sure student and host family sign a double placement form.</li>
  </ul>
</ul>


