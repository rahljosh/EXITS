<p><em>(If your student requires a convalidation and is not from one of the countries listed above, please contact your ISE Area Representative</em>)</p>
<p><strong>Participating in Winter Sports:</strong> Many of our students will experience their first snowfall here in the U.S. and may want to try winter sports. Please make sure that students have sport-appropriate clothing and safety gear. Recreational downhill skiing, snowboarding, skating, sledding and cross country skiing are all covered winter sports on the Insurance plan. However, snowmobiling is covered only if the student is a passenger and exchange students should never operate a motorized vehicle.</p>
<p><strong>Holiday Travel: </strong>School vacations and holidays are upon us and some families are planning vacations with their exchange student. Please notify your ISE Area Representative if you will be taking your exchange student out of town.</p>
<p>If your vacation plans include <strong>international travel</strong>, it is critical that you contact your representative well in advance of your trip. In order to travel internationally, your exchange student will need to get their travel authorization signed on their US entry documents (DS-2019). We'll also need to confirm that the student has a multiple entry visa. Failure to obtain this signature can create substantial problems upon your return to the US and it could mean that your exchange student might not be allowed to return to the US. Please also keep in mind that the exchange student&rsquo;s insurance does not cover international travel so they should secure a policy before they travel. There are many online travel insurance providers that offer reasonably-priced policies.</p>

