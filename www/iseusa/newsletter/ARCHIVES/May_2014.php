<h1>Facilitator Square</h1>
<h3>Saying our goodbyes</h3>
<p>Below is the newsletter we are sending to host families.  Key issues to focus on this month are &ldquo;reverse culture shock&rdquo; and arranging the student&rsquo;s return home.</p>
<p>Each year, we hear of cases where the student and family are having a near perfect exchange experience and then it seems to suddenly go wrong with just a couple weeks left on the program.  A lot of this is related to what psychologists refer to as &ldquo;separation anxiety&rdquo;.  Students and their host families experience a wide range of emotions as students and families begin to think about the eventual return home.</p>
<p>Nerves are raw and mild disagreements can become big arguments and ruin the ending of the program.  It&rsquo;s important to recognize these situations and try to talk the student and family through these issues.  Often it just takes a few minutes to talk about all the good things that have happened during the program and to get the student and family to recognize that they are just feeling anxious.  If the situation becomes more significant, make sure you involve your Regional Manager or Facilitator.</p>
<h3>Host Family Newsletter</h3>
<p>First of all, we at ISE would like to take a moment to thank all of our wonderful host families.  Without your generosity, caring and patience we would never be able to offer these life-changing opportunities to students. We know that the past year has had its challenges and ups and downs, but you&rsquo;ve contributed to the development of a young person and have helped build understanding and close bonds between the US and other nations that will last for decades and longer.</p>
<h3>Reverse Culture Shock</h3>
<p>As your student starts to think ahead to their return home, they will likely feel a flood of emotions.   This comes from leaving their host family and many new friends behind as well as their return home to their family, friends and home country.  This anxiety is completely normal and you may see your student be a little sad, withdrawn or maybe more emotional.  If you do see this, make sure you talk things through with them and don&rsquo;t over-react.  If you do see more significant acting out or other issues, please contact your ISE Area Representative and make them aware of the situation.</p>
<p>Host family members will also feel some separation anxiety, so be careful to recognize this in yourself or your family members.  Maybe host siblings start arguing more or you don&rsquo;t have as much patience with your exchange student or other family members.  This is common at the end of the program as families start to think about their newest family member soon leaving.  Just try to be more patient at this time and talk things through with your family.</p>
<p>You and your exchange student will be receiving a survey approximately two weeks before the end of the school.  The information collected in this survey helps us understand how we can improve the program and better prepare future students and host families.  It takes about 10-15 minutes to complete and the information collected is very important.  Families and students who complete the survey will be entered into separate drawing for iPads.</p>

<h3>Things to resolve before your student returns home:</h3>
<p><strong>Insurance and medical bills:</strong>  If your student has any outstanding bills from medical treatment while on the program, please make sure you ISE Area Representative is made aware of this.  The student needs to make sure these bills are paid prior to departure</p>

<p><strong>Other bills or items:</strong>  Make sure that any phone bill is addressed and any borrowed items, including school books and uniforms, are returned.</p>

<h3>Return travel planning</h3>
<p><strong>Luggage:</strong>  most students will return home with more luggage than they came with.  This can result is <strong><em>substantial excess baggage charges</em></strong> at the airport.  Some tips on how to avoid or plan for these charges:</p>
<ul>
  <li>Make sure student <strong>do not pack</strong> their passport, plane tickets, wallet, ID, or other important items in their checked luggage</li>
  <li>Some items can be shipped home prior to departure</li>
  <li>Review the baggage allowance and fees for the student&rsquo;s return flight. This will be on the airline&rsquo;s website.  Make sure you&rsquo;re checking the international flight details.</li>
  <li>Weigh the student&rsquo;s baggage on a bathroom scale to make sure that it is under the maximum weight.  Some airlines also charge for oversize bags so measure the bags to make sure they don&rsquo;t exceed the limit.</li>
  <li>If the bags are too heavy consider the following:</li>
  <ul>
    <li>Should the student donate some of their clothing?</li>
    <li>Can some items be shipped home?</li>
    <li>Make sure the student brings cash to the airport to cover the excess baggage charges.  These can be as much as $300 or more.</li>
  </ul>
</ul>
<h3>Return flights:</h3>
<ul>
  <li>Arrive at the airport 3 hours before the flight departs.</li>
  <li>Make sure you have a clear plan on who will be taking the student to the airport</li>
  <li>If the student has a cell phone, make sure it is completely charged and that they have their charger in a carry-on bag.  This will be very helpful if they experience a flight delay, cancellation or missed connection.</li>
</ul>
<p>Make sure students have ISE&rsquo;s 800 number – 800-766-4656, your phone number and their Area Rep&rsquo;s phone number.
