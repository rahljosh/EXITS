<h1>Facilitator Square</h1>

<p>The Facilitator Team thanks you for opening your home to a foreign exchange student.  We hope you've found your host family packet and manual helpful and encourage you to review this information in detail.   In addition to the family packet, we'll be presenting timely topics throughout the year via this newsletter and hope this additional information will help you assist your exchange student in their adjustment to life in the US.</p>
<h3>Topics</h3>
  <p><strong>Establish House Rules:</strong> Establish rules and expectations with your exchange student early on (fill out form together and put up on your fridge) and review ISE program rules and program policies in the ISE handbook. Be sure to fill out the Computer Usage Expectations Worksheet as well.  We would also recommend against including exchange students on your cell phone plans as this has resulted in unexpected bills for families in the past.</p>

<p><strong>Adjustment</strong>: Many students go through feelings of homesickness when they arrive and experience highs and lows in their first few weeks or months. Your family will also notice adjustment issues as you take on a new family member. </p>
<p><strong>Insurance Info:</strong> Students on the ISE high school program are covered under an insurance plan issued by Global Secutive and claims are managed by United Healthcare. Please take a moment to visit the Global Secutive website (<a href="http://www.esecutive.com/index.php">http://www.esecutive.com/index.php</a>) and review options for doctors in your area.  Please also identify a local urgent care clinic. While all communities do not have urgent care clinics, it is usually a more preferable option than an emergency room as the service usually happens faster and students aren't subject to special emergency room visit charges. Urgent care clinics can usually treat sprains, broken bones, minor cuts and burns and other non-life threatening situations and emergency rooms are better suited for significant injury, breathing difficulties and other significant emergency health concerns. Please contact the ISE office if you have any insurance questions at <a href="tel:800.766.4656" target="_blank">800.766.4656</a>. Please have your student make a copy of their insurance card and keep the copy in a safe place and make sure the student keeps the card with them at all times.</p>
<p><strong>Communication:</strong> We encourage you to contact your area rep, no matter how big or small the issue. The earlier ISE is made aware of any situation, the more quickly we can address an remedy it.</p>
<p>Your local area representative is your most important contact for ISE. However, our facilitator team, based in ISE's home office, is also there to assists area representatives, students and host families with any questions or challenges that arise during the program. The facilitator may contact your exchange student or you directly if we feel this will be helpful. We recommend that your family address any issues to your local area representative, but when needed, you can contact our home office at 800-766-4656.</p>

