<h1>High-Five</h1>

<h3>Giving a High-Five to the Dixon Family!  They are located out of Hartford, MI and have been a host family for 8 years.</h3>
<p><strong>Q1: <em>What made you want to be a Host Family? &nbsp;</em></strong></p>
<p class="grey"><em><strong>A: </strong>We became a host family by accident. Our son became friends with a Brasilian student, Roberto, in 2007. He encountered a difficult situation, the boys put their heads together to find a way for him to live with us and our journey began. 13 students later here we are.</em></p>
<div class="hrLine"></div>
<p><strong>Q2: <em>What&rsquo;s the best part about being a Host Family?</em></strong></p>
<p class="grey"><em><strong>A: </strong>The best part of hosting would be all of the exposure that our children get to other cultures. Our oldest daughter is Brasil obsessed. We have discovered that it is very much a learning experience for the student and the host family.</em></p>
<div class="hrLine"></div>
<p><strong>Q3:&nbsp;<em>What do you usually do with your student(s) for fun?</em> </strong></p>
<p class="grey"><em><strong>A:</strong> We are in a small town so everyday fun can range from a movie or a crazy dinner table conversation. Shopping is 35 minutes away so we don't do that too often. Our students leave knowing what life in small town America with a working class family is like.</em></p>
<p><strong>Q4: <em>Any great stories you want to share?</em></strong></p>
<p class="grey"><em><strong>A: </strong>Oh my goodness, there are so many.  Things like explaining what gravy is.  We had one student in our family, Danilo, that his parents sent us all matching Brasil sweats and jackets.  That is our unofficial family Christmas picture.</em></p>

<p class="grey"><em>We had a boy from South Korea, Jae, and we told him he had a good singing voice and from that time on he sang constantly!  Driving his roommate another boy from Brasil, Bernardo, absolutely insane. We are also foster parents so our students have seen how that system works. Our daughter Delaney and Nadja, from Germany walked about a mile to McDonalds in a blizzard.</em></p>  

<p class="grey"><em>Our girls, Nadja and Barbara, were amazing when we were new to fostering. So many stories it truly is hard to pick one. We love all of our foreign family so very much.  Each one has a special place in our hearts!</em></p>
<div class="hrLine"></div>
<p><strong>Q5: <em>Any Advice for other host families?</em></strong> &nbsp; </p>
<p class="grey"><em><strong>A:</strong> Treat them as if you would your own children.  The exchangers are special but they are also a part of your family's day to day life. Make sure your house rules are clear, they are teenagers and they try to push boundaries. Most of all enjoy the semester or academic year. The students program goes by so quickly. Tell them to take lots of pictures, journal or whatever they can to take special memories home and share with their natural parents parts of our culture. Enjoy and encourage them to try a little bit of everything their schools have to offer!</em></p>

