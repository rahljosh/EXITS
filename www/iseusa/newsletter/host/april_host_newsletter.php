<h1>Facilitator Square</h1>
<p>We&rsquo;re on the homestretch and headed toward the finish line.  </p>
<p>This can often be one of the best times of the year for you and your exchange student.  By this point, they are likely very comfortable with their English skill and have truly become part of your family.  It&rsquo;s a great time for family trips, visits to relatives and lots of outdoor activities.  Take advantage of the remaining weeks.  Make sure your student also uses their short time remaining on the program most efficiently.  Keep them busy and involved in family activities. </p>
<p>While this is one of the best moments of the program, it&rsquo;s also a time that presents many temptations to students that can cause problems.  To help make sure your student finishes the program successfully, here are some pointers:</p>
<p><strong>Keep them focused on the present.</strong>  As students get closer to going home, some might start to think ahead to their return home.  This can cause a &ldquo;lack of focus&rdquo; on their responsibilities to their host family, schoolwork and activities.  Make sure your exchange student doesn&rsquo;t start to dramatically increase their communication with their natural family and friends from their home country.  It&rsquo;s OK for them to start to make some preparations.  You just need to gently remind them that they are still on the program and to continue to follow their progress in school.</p>
<p><strong>Address any &ldquo;acting up&rdquo; issues immediately.</strong>  Some student&rsquo;s think they can take advantage of the fact that they are going home.  They might start acting out and being disobedient.  Some of this might just be typical teenage behavior.  If your student does suddenly becomes defiant or starts engaging in behavior that is causing problems in your home, you should address these behaviors immediately.  Make it known that they still need to follow host family rules until the day they depart.  If issues don&rsquo;t get better quickly, make sure you notify your ISE area representative.</p>
<p><strong>Be wary of temptations.</strong>  As we mentioned in last month&rsquo;s newsletter, spring is also a time for celebrations, family gatherings, proms and going away parties.  Sometimes these parties can present temptations for students to drink or use drugs.  When your student is going to a party or event, please make sure there will be adult supervision at the event.  Please remind your student that they cannot drink on the program.  Each year we have the unfortunate task of sending a handful of students home weeks from program completion because of drinking incident.</p>
<p>If you haven&rsquo;t already done so, please review out some of the previous month&rsquo;s detail on Convalidations and Natural Family visits.</p>
