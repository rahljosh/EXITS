<h1>High-Five</h1>

<h3>Giving a High-Five to the <strong>Bell Family!&nbsp; They are located out Long Prairie, MN and have been a host family for 6 years.</strong></h3>
<p><strong>Q1: What made you want to be a Host Family?</strong></p>
<p class="grey"><em><strong>A: </strong>We wanted to teach our children about other cultures and other languages.  My husband and I have both traveled and spent time in Europe and plan on taking the kids back someday. We wanted them to understand that the world is a whole lot bigger than just Long Prairie, MN.</em></p>
<div class="hrLine"></div>
<p><strong>Q2: What’s the best part about being a Host Family?</strong></p>
<p class="grey"><em><strong>A: </strong>We LOVE having family all over the world! All of the kids that have stayed with us are still like family to us and we still talk to every one of them regularly.  When we started hosting, our children were 1 and 3 and they absolutely love having big brothers and sisters from all over the world. They look forward to visiting all of them at their homes someday and meeting their families as well.</em></p>
<div class="hrLine"></div>
<p><strong>Q3: What do you usually do with your student(s) for fun?</strong></p>
<p class="grey"><em><strong>A:</strong> We always take a weekend to go to the Mall of America and stay in a hotel in the cities. We visit various museums and go up to the North Shore. Some of our students have taken a train trip to Seattle, gone to MN Wild games and the Minnesota State Fair.  Most recently, we have started a family mission to get to every state park in Minnesota.<br /> <br />

Our students experience camping adventures and learn (if they don't already know) how to set up a tent and cook over a fire. At the parks, we hike, canoe, and always do any geocaches in the area. Every one of our students is immediately whisked into our large family events within hours of arriving; they have gone to weddings, Labor Day parties, camping (6 hours away) or family gatherings. We usually don't break them in slowly. :) We literally go on an adventure almost every single weekend! Our students all know this ahead of time, because that's what a "normal family experience" is when you live with us.
</em></p>
<div class="hrLine"></div>
<p><strong>Q4: Any great stories you want to share?</strong></p>
<p class="grey"><em><strong>A: </strong>Our daughter is in Kindergarten this year and they always do a postcard project to see how many states they can get postcards from. They send out a letter asking for information about your state. Most kids receive 1-3 postcards. Cora received 35! At least 10 of them were not from other states but other countries, from our children abroad as well as their families! She also got some from other students that were here but lived with friends of ours. It was the hit of the class!<br /><br /> 

Also on one of our many camping trips, our children were getting ready for bed and were able to go around the campfire and say goodnight to everyone (approximately 16 people) in a different language for every person! How awesome is that?! They were 5 and 7 at the time. I don't know of many other kids that could say that. It was a very proud moment as a mother and affirmed that my goal in hosting has truly paid off and will continue to do so. 
</em></p>
<div class="hrLine"></div>
<p><strong>Q5: Any Advice for other host families?</strong></p>
<p class="grey"><em><strong>A: </strong> Many people are hesitant to host because they feel their children are too young.  We tell them that is NOT AT ALL true!  We would not have changed a moment of this (good or bad) as it has been a great adventure that has benefited our family in so many ways. We are truly blessed to be able to continue to host students every year. <br /><br />

We feel our kids have benefited as they have learned early on what it really means to share and that they aren't always in charge.  They have grown up thinking it is completely normal to host a student 
and can't understand why people wouldn't do this! <br /><br />

Our children have learned to be unselfish and to volunteer their time and efforts to helping others. They have grown to be kids that never look differently at someone whose skin is a different color or has an 
accent. They have learned to slow down and find ways to communicate with those that are culturally different than themselves. The foods they have tried go well beyond just getting them to eat broccoli, they have learned to eat (or at least try) many different kinds of food without batting an eye.  They are learning to accept everyone for who they are.
</em></p>

