<h1>High-Five</h1>

<h3>Giving a High-Five to the Howey Family!  They are located out of Baxter Springs, Kansas and are hosting for the first time.</h3>
<p><strong>Q1: What made you want to be a Host Family?</strong></p>
<p class="grey"><em><strong>A: </strong>We hosted an exchange student back in 1996, but just for two weeks.  We also have hosted many players on ball teams from other states, when they were here for tournaments. So we were used to having people in our home at times.</em></p>

<p class="grey"><em>It is kind of a long story, but we felt like we could do missions work in our home through the students, and we felt like God had a part in how we came to host the students through our representative.  We also enjoy people from everywhere, so we thought it would be very interesting to host.</em></p>
<div class="hrLine"></div>
<p><strong>Q2: What’s the best part about being a Host Family?</strong></p>
<p class="grey"><em><strong>A: </strong>I think sharing each other’s everyday living, cultures, customs, and foods.</em></p>
<div class="hrLine"></div>
<p><strong>Q3: What do you usually do with your student(s) for fun?</strong></p>
<p class="grey"><em><strong>A:</strong><em> We have gone places in our surrounding area, took them to some of our highlights around us.  We have been to Branson, Mo. We go bowling, movies, and just family fun. We stay involved in whatever they are involved in their school.</em></p>
<div class="hrLine"></div>
<p><strong>Q4: Any great stories you want to share?</strong></p>
<p class="grey"><em><strong>A: </strong>We had so many good times with them, to many to share really, it's been an overall great experience.</em></p>
<div class="hrLine"></div>
<p><strong>Q5: Any Advice for other host families?</strong></p>
<p class="grey"><em><strong>A: </strong> We have two exchange students, and it is very important to make sure you include both of your students in on your conversation, even when only one of them is asking advice or questions, so the other can learn or comment, and be in on the conversation, so they are both having the best experience they can have. Very important to do a lot of listening, and learn about them, their families and most of all about their heart, they are no different than what our children were, they want your love and attention.  It's not about us hosting the students, it's about opening our home for they have a real American Experience.  Loving it very much!</em></p>

