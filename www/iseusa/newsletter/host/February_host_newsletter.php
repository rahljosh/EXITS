<h2>Reminders</h2>

<h3>Traveling with your Student</h3>

<p>While many schools have already had their spring break, others will be coming up and families will often want to travel with their exchange student.  This is greatly encouraged and allows the student to experience more of the US.  Please notify your ISE Area Representative of your travel plans at least two-weeks prior to any domestic trip and one month prior to any cruise or international trip, including Canada and Mexico.</p>
  
<h3>Cruises and International Travel</h3>
<p>If you are taking a cruise or traveling out of the country, students must have a signed travel authorization from ISE.  Each year we get panicked calls from families that are not able to board a cruise or are having trouble returning to the US because the student didn’t have an approved travel authorization.</p>
<p>It’s up to the individual family, but it is OK for a host family to expect the exchange student to cover significant costs associated with a trip including airfare and their own hotel room and meals.</p>

<h3>Visits from Natural Family</h3>
<p>At this time of year, natural family members will be missing their sons and daughters and want to visit them in the US.  Natural family visits are not allowed during the ISE exchange program as we feel that it disrupts the student’s cultural acclimation and has the potential to cause problems.  Natural parents have all been notified in advance that these kinds of visits are not allowed. If you are asked by your student or their parents about a visit, please notify your ISE Area Representative and we’ll follow-up with the parent.</p> 
<p>Natural parent visits are allowed at the end of the program, but it is up to each host family to determine whether this is acceptable and what the arrangements might be.  So for example, maybe you are OK with the natural family visiting, but don’t feel there is room in your home for one or more extra guests.  In that case, it is perfectly OK to suggest nearby hotels or B&Bs where the natural parents can stay.
