<h1>Facilitator Square</h1>
<p><strong>January 2014</strong></p>
<p>Dear ISE Host Families:</p>
<p>As we begin the slow warm-up from what has been a seemingly unending winter, we start looking ahead toward the conclusion of many students’ programs. They aren’t going home quite yet, but they should start preparing for their eventual return home. Things to consider at this time are return flights home and in the case of some students, educational record convalidations.</p>

<h2>New Topics</h2>

<h3>Return Flights Home</h3>

<p>Students are expected to return home within five days of their last day of school. While some students would like to stay on longer, we find that we have fewer issues if students return home soon after school ends. Sometimes students will join natural family members who are traveling in the US, and that is acceptable. The student must realize, however,  that they are no longer to be considered on the program after the five day departure window. Exchange students should confirm their return flights with their agent or whoever booked their flight to assure that it still matches with their school departure dates. Host families and students should also coordinate travels plans to the airport for their departure. If you are unable to provide transportation to the airport for your student’s departure, please contact your ISE Area Representative.</p>

<h3>Convalidations</h3>

<p>If you’ve hosted a semester student from Brazil, Ecuador, Italy, Spain or Mexico, you may have already heard them mention something about a “convalidation”.  A convalidation is an official certification of a student’s transcript that they will need to present to their home school in order to get credit for the classes they took in the US.  It is the students’ responsibility to take care of their own convalidation process, but they may need your help in securing some of the documents since they will often have to return home before their transcript is available.  Usually, the host family just needs to forward the notarized transcript to the exchange student.  The convalidation process varies from country to country and links to the process are here:</p>

