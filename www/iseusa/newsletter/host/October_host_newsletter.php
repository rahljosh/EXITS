<h1>Facilitator Square</h1>

<p>As we move into fall, the "honeymoon" phase is over for students and their host families. Families are now noticing that their exchange student has begun to settle into their new surroundings. Your family has also become accustomed to the student as a member of the family. Your exchange student is no longer a guest but a truly integrated member of your family. He or she is living by the same rules as any family member, attending family functions and performing some household chores (something they may have never experienced before).</p>
<p>Exchanges students start to understand the various personalities within your family and start to understand "family jokes", traditions and other customs. Perhaps they've become fans of your favorite local sports teams or have taken up activities shared by other members of your family. Communication should be now easier and more open. While this has many benefits, it perhaps comes with challenges as well.</p>
<p>At this point in the program, some students may start to test boundaries, something that is universal among teenagers. If issues do arise, try to address them early and often. Remind your student of your expectations at home. Remind them that they are now family members, not guests. Show them ways to help out around the house.</p>
<p>Treat your exchange student just as you would another member of the family. If you have concerns that your student has not adjusted well to your family, please be sure to communicate your concerns with your area representative.</p>
<p>While it may seem too early to think about this, many students and host families are making plans for travel over the Thanksgiving and winter school holidays. We encourage you to take your exchange student with you on family trips. If your family trip requires exceptional expenditures e.g. airfare, hotel rooms, ski lift tickets, amusement park admissions or unusual expenditures, it is acceptable to expect a student to share some of this expense. Please let your area representative know so that they can make arrangements with the student's natural family.</p>
<p>Students sometimes also want to go on trips of their own choosing over the holidays. We want to remind everyone that while students may participate in school, church, ISE and other organized group trips. They may not travel independently or without an approved adult chaperone. If your student makes a request to travel independently, please have them contact their area rep.</p>
<p>It is also at this time of year that the natural family will request their child (exchange student) join them on a vacation. <strong style="color: #F00;">This is not allowed.</strong> Students are participating in an exchange program. Having them leave the program to rejoin their natural family, even temporarily, negatively impacts their program experience. If you receive a request from your exchange student or his/her family, please contact your area representative and they'll notify the home office.</p>
<p>Thank you once again for hosting an exchange student. It's because families like yours open their homes and hearts that together we're to give students these life-changing experiences. If we can ever be of any assistance, please to not hesitate to contact your area representative or the ISE home office.</p>

