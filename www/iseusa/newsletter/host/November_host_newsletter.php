<h1>High-Five</h1>

<h3>Giving a High-Five to the Carraway Family!  They are located out of Wesson, MS and have been a host family for 5 years.</h3>
<p><strong>Q1: <em>What made you want to be a Host Family? &nbsp;</em></strong></p>
<p class="grey"><em><strong>A: </strong>Hosting exchange students is volunteerism at its best.  We love sharing our wonderful American lifestyle that we are blessed to have with students from around the world.  Every student that we have hosted are amazed at the hospitality of our community.</em></p>
<div class="hrLine"></div>
<p><strong>Q2: <em>What&rsquo;s the best part about being a Host Family?</em></strong></p>
<p class="grey"><em><strong>A: </strong>My family loves to learn about different cultures from around the world.  Since we will probably never have the opportunity to travel, we bring the world to us.  I do have big plans to visit all my international babies one day and I love when they come back for visits with their families!  Thank goodness for social media because I love keeping up with my youngins after they leave my nest!</em></p>
<div class="hrLine"></div>
<p><strong>Q3:&nbsp;<em>What do you usually do with your student(s) for fun?</em> </strong></p>
<p class="grey"><em><strong>A:</strong> My husband and I have three teenagers so we are always active with school and community events.  We try to visit as many states in our region with our students.  New Orleans is always a fun trip!  Cajun food and pralines are always a hit!!</em></p>
<p><strong>Q4: <em>Any great stories you want to share?</em></strong></p>
<p class="grey"><em><strong>A: </strong>We have been blessed with some awesome students and it's hard to pick one story.  The one that stands out the most is when our son won the Geography Bee at school because he knew the capital of Brazil since our student at the time was from Sao Paulo!</em></p>
<div class="hrLine"></div>
