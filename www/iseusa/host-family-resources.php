<h1>Host Family Resources</h1>
<div style="float: right; margin: 10px;"><img src="images/host-family-resources.jpg" width="180" height="254" /></div>
<p>We have set up a network that includes your Area  Representative, Regional Manager and the ISE New York Office to support  you with every stage in the hosting process.  Your ISE Area  Representative is the person who has arranged the student's placement  and secured permission for the student to attend the local high school.  </p>
<p>Your ISE Representative  will contact you and the student monthly.  You may contact your  representative if questions or issues arise or request a meeting at  anytime in between.  Your ISE representative will also contact the  school to check the progress of the student.  IF YOU  OR THE STUDENT DO  NOT HEAR FROM YOUR AREA REPRESENTATIVE, please notify ISE at <a href="tel:800-766-4656" value="+18007664656" target="_blank">800-766-4656</a>.   </p>


 <div class="dotLine">&nbsp;</div><br />
<h2>What We Expect of the Host Family</h2>
<h3>Before the student arrives</h3>
<ul>
<li>Host families participate in training and orientation sessions offered by ISE.</li>
<li>Establish communication with the student.</li>
<li>Understand the program rules that the student must follow.</li>
<li>Know how to contact your Area Representative, Regional Manager, ISE New  York office and local community services (police, hospital, etc) in case  of an emergency.</li>
</ul>
 <div class="dotLine">&nbsp;</div><br />
<h3>During the Exchange</h3>
<div class="two-col">
<ul>
<li>Provide a safe and welcoming environment for trust and friendship to develop between the student and your family.</li>
<li>Support and make the student feel like a part of your family, with the same privileges and obligations.</li>
<li>Encourage the student to learn and participate in the ways of your home.</li>
<li>Provide room and board for the student. All students must have their own bed. If the student must share a room, it should be with a child of the same gender and similar age.</li>
<li>Provide a quiet place in the home where the student may study.</li>
<li>Recognize the student's birthday and other special occasions.</li>
<li>Ensure the student knows how to contact family members, friends, and other support networks.</li>
<li>In case of an emergency, have access to a copy of the student's passport and insurance card.</li>
<li>Voice any concerns and questions regarding the student to the ISE representative, including serious homesickness, difficulty adapting to family life or school, or illness.</li>
<li>Maintain close contact with your ISE area representative, and address problems and concerns quickly.</li>
<li>Exercise supervisory and parental responsibility to ensure the student's well-being.
</li>
<li>Encourage involvement in community life by introducing the student to neighbors, friends, and local groups.</li>
<li>Teach the student about the local culture, and learn about the student's culture.</li></ul><br></div>
