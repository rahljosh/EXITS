    <h3>International Exchange Students Pick up the &quot;Tab&quot; for Charity</h3>
<p>Long Prairie, MN &#8211; November 20th, 2013 &#8211; Exchange students from International Student Exchange (ISE), collected over two boxes full of soda tabs for the local Ronald McDonald House Chapter. The soda tabs can be resold for funds.</p> 
  <div class="clearfix"></div> 

<p>ISE, a student exchange organization, runs a community service program called Project HELP. Each foreign exchange student must do 5 hours of community service.  Most students go above and beyond that number. In Minnesota, the students collect soda tabs and donate them to the Ronald McDonald House Charities for their Project HELP contribution.  
<a href="#" id="example5-show" class="showLink" onclick="showHide('example5');return false;">Read More &#187;</a></p>
