 <p>The exchange students live with a host family that provides a bed and several meals a day, as well as a loving environment. The host family does not receive any compensation for doing this. Wayne Brewer, CEO of International Student Exchange states. "Since our host families aren't paid, they do it to bring a new culture into their house. Most host families are amazed on how great the experience is."</p>
 <p>The students participate in school sports, theater, clubs and community service.  They are treated as a family member rather than a foreign exchange student.</p>
