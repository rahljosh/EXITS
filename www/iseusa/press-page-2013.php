    <h3>A Foreign Exchange Student Volunteers with the Salvation Army</h3>
<p>Loretto, TN &#8211; December 18th, 2013 &#8211; Antonio Giribaldi, an exchange student, volunteered along with his host family to help the Salvation Army. Antonio stood outside in a shopping complex for hours to raise money.</p>  
  <div class="clearfix"></div> 

<p>Antonio came to the US from Italy through International Student Exchange (ISE). He came here to have a new experience while refining his English skills. Antonio settled in very nicely. He joined his football team, became friends with a few people at school and even got to know his new neighbors. He really enjoys it when his extended family visits. 
<a href="#" id="example7-show" class="showLink" onclick="showHide('example7');return false;">Read More &#187;</a></p>
<div id="example7" class="more7">
<div style="width: 240px; margin-right: 25px; margin-left: 10px; float: right;">
<img src="images/pressKit/salvationArmyRinger.png" width="200" height="267" alt=""/>
<p class="smSize">&nbsp;</p>
</div>
<p>"I have become good friends with my host nieces. They are a lot of fun to hang out with. My host family makes it very comfortable to live here", stated Antonio.</p>
<p>It was his host sisters that got him involved with the Salvation Army. Antonio had volunteered back in Italy with the local Boy Scouts. He said he heard of the Salvation Army before, but never had any interaction with them. His host sisters signed him up to stand outside for hours to collect money.  
"It was very cold, volunteering to collect money. In the end, I felt like I really helped some people" explained Antonio.</p>
<p>Antonio plans to do more community service in the future.  He’s not sure what his next opportunity will be, but he likes giving back to his new community.  
"I really enjoy living in the US. I hope to get more involved in my community throughout the year." said Antonio.</p>

