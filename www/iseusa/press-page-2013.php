    <h3>An Exchange Student Volunteers with FEMA to Help During the Colorado Floods</h3>

<p>Longmont, CO &#8211; November 1st, 2013 &#8211; Ana Beatriz Correa, an ISE exchange student, volunteered with FEMA to assist her community after the Colorado floods back in early September. The floods caused damaged to over 20,000 homes while destroying roads, bridges and businesses.</p> 

<p>Ana, an ISE exchange student, is from San Paulo, Brazil. She came to the US to experience a new culture and to refine her English speaking skills. She was placed with the Spencer family, who live in Longmont, CO. Shortly after Ana settled into her new town, she experienced a major storm that left her town in a crisis.<a href="#" id="example4-show" class="showLink" onclick="showHide('example4');return false;">Read More &#187;</a></p>
<div id="example4" class="more4">
<p class="p1">"I was on a hill, so I wasn't nervous about my host family house. I did walk down to see the flooding. I was shocked to see roads covered with water and bridges destroyed," explained Ana.</p>
    <p>Ana didn't do much the next couple of days.  School was cancelled for the week. Ana also kept up with family from Brazil, assuring them she was safe. By the 3rd day, Ana woke up and decided she wanted to help her community.  She had volunteered before back in Brazil at an adoption agency.  Ana realized that her community needs her.</p>  
    <p>"Why should I sit in my bedroom if I could be out doing something?" Ana asked herself.</p> 
    <p>She then told her host mom the next day that she wanted to help. Her host most called a friend she knew that was involved in the community. Ana was able to get connected with FEMA. She was assigned to take supply orders for families that had their house damaged. She never heard of FEMA before.  There was nothing like FEMA in Brazil.</p>  
    <p>"After the first day, I thought I was really helping people. It made me go back and help again," commented Ana.</p>
    <p>Ana told us things are starting to get back to normal in Longmont, but there is still a lot of work to do.</p>  
