<p>Hauppauge, NY &#8211; September 17th, 2013 &#8211; The International Student Exchange (ISE), which has been placing international students since 1982, will place nearly 2,800 exchange students in 49 different states for the year 2013.</p> 
<p>"Placing this many students showcases the generosity of the people in this country.  Our host families aren't compensated for opening up their home to these students." said Bob Keegan, Executive Director....<a href="#" id="example1-show" class="showLink" onclick="showHide('example1');return false;">Read More &#187;</a></p>
