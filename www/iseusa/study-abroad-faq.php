<div class="blueLine"></div>
<div id="Q1">
<h3>How can I acquire a visa to study abroad?</h3>
<p>In order to study abroad, each student must receive a student visa from the country in which they plan to visit. A visa is needed for both school year programs and semester programs. To obtain a visa, a student must schedule an appointment with a Consulate of the host country, located in the United States. This process may take as long as four weeks and, in some cases, may entail a fee. This fee and other travel expenses incurred in acquiring this visa will not be covered by your Outbound program fee and will be at the student's expense. Note that a visa may not be required for short stay programs.</p>
  <p class="smSize"><a href="#top">Return to top &#8593;</a></p>
</div>
<div class="blueLine"></div>
<div id="Q2">
<h3>What age must a student be to participate in the ISE Outbound Program?</h3>
<p>ISE Outbound program works with students between the ages of 15 and 18 1/2.</p>
  <p class="smSize"><a href="#top">Return to top &#8593;</a></p>
</div>
<div class="blueLine"></div>
<div id="Q3">
<h3>What are the language requirements of the Outbound program?</h3>
<p>Each program applicant must have at least two years of experience speaking, reading, and writing in the language of the host country. Language immersion programs such as &quot;Rosetta Stone&quot; will be considered on a subjective basis.</p>
  <p class="smSize"><a href="#top">Return to top &#8593;</a></p>
</div>

<div class="blueLine"></div>
<div id="Q4">
<h3>How are host families abroad selected?</h3>
<p>Students abroad will live with host families who have undergone thorough background checks. A representative of ISE's overseas partner agency will be available to address any concerns of the student while abroad. An International Student Exchange representative will be available to address concerns of the natural family. The ISE representative and overseas agent will work together to address any and all issues that may arise in order to ensure a safe and rewarding cultural sharing experience.</p>
  <p class="smSize"><a href="#top">Return to top &#8593;</a></p>
</div>

<div class="blueLine"></div>
<div id="Q5">
<h3>Whom will provide insurance while abroad?</h3>
<p>International Student Exchange will provide insurance for the student while abroad. These insurance expenses will be covered by the cost of your program fee. This program fee also covers homestay, room and board, and school placement.</p>
  <p class="smSize"><a href="#top">Return to top &#8593;</a></p>
</div>

<div class="blueLine"></div>
<div id="Q6">
<h3>When is complete program payment due?</h3>
<p>Program fees are due in their entirety thirty (30) days after formal acceptance into the program.</p>
  <p class="smSize"><a href="#top">Return to top &#8593;</a></p>
</div>

<div class="blueLine"></div>
<div id="Q7">
<h3>Is Private schooling an option?</h3>
<p> In certain countries, private school options will be offered to the Outbound student. These private school options are optional. If the private school option is accepted by the student, the student is liable for any accompanying tuition fees, which are not included in the cost of the program</p>
  <p class="smSize"><a href="#top">Return to top &#8593;</a></p>
</div>

<div class="blueLine"></div>
<div id="Q8">
<h3>What if, in order to apply for a visa, I must provide a flight itinerary, but I can&rsquo;t purchase return flights yet?</h3>
<p>In order to apply for a visa, certain consulates require a complete flight itinerary before providing a visa. In the event that a plane ticket cannot yet be purchased, because the date of the program's end is too far in advance, a placeholder date may be put in for the purposes of acquiring the visa. The actual departure flight may be purchased at a later date, with the proper information being forwarded by ISE to the overseas agent at that time.</p>
  <p class="smSize"><a href="#top">Return to top &#8593;</a></p>
</div>

<div class="blueLine"></div>
<div id="Q9">
<h3>What must I include with my completed application?</h3>
<p>Each Outbound application must include a $100, non-refundable application fee. Each application must also include a $750 program deposit. This $750 will go towards the student's program fee and will be refunded if the student is not selected for the Outbound program, or must back out of the program prior to visa acquisition and departure.</p>
  <p class="smSize"><a href="#top">Return to top &#8593;</a></p>
</div>

<div class="blueLine"></div>
<div id="Q10">
<h3>Will these schools and host families abroad be English-speaking?</h3>
<p>Note that unless the host country is English-speaking, or otherwise noted, these schools are not English-speaking schools in a foreign country. Host families and schools will be authentic in the sense that the foreign language (if applicable) will be spoken exclusively.</p>
  <p class="smSize"><a href="#top">Return to top &#8593;</a></p>
</div>

<div class="blueLine"></div>
<div id="Q11">
<h3>Does ISE offer financial aid of any kind?</h3>
<p>No scholarships towards the program are offered to any applicants under any circumstances. The primary fee of this program pays the international agent and does not involve ISE.</p>
  <p class="smSize"><a href="#top">Return to top &#8593;</a></p>
</div>

